package day1;

public class LearningIfStatement
{
	public static void main(String[] args)
	{
		int a=10, b=10;
		
		if(b>a)
		{
			System.out.println("b is greater than a");
		}
		else if(b<a)
		{
			System.out.println("b is less than a");
		}
		else
		{
			System.out.println("b equals to a");
		}
	}
}
